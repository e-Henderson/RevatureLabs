
public class MathematicalOperators {
	public static void main(String[] args) {
		int x=5;
		int y =3;
		int z;
		z=x+y;
		System.out.println(z);
		
		z=x-y;
		System.out.println(z);
		
		z=x*y;
		System.out.println(z);
		
		z=x/y;
		System.out.println(z);
		
		z=x%y;
		
		System.out.println(z);
		
		//diff dat-types
		float f= 3.6f;
		int i =22;
		int result = (int)f +i;//notice that f is cast
		System.out.println(result);
		
		
	}
}
